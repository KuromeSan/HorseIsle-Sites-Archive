<B>Game Content:</B><BR>
Horse Isle has been designed from the ground up to be an enjoyable, safe and educational experience. 
Players of all ages enjoy Horse Isle.  All content in the game is completely non-violent.  This LifeCycle server does allows breeding and cross-breeding of horses.  Horses are born and age, and eventually do die. Please make sure this is appropriate for your child, as some can get quite attached.  Our 'Eternal' version keeps horses alive forever.<BR>
<BR>
<B>Educational Benefits:</B> (Almost all of this occurs without the child realizing it!)<BR>
<LI><I>Puzzles:</I> Many puzzle minigames require memory, logic, planning and problem solving.</LI>
<LI><I>Reading:</I> Quests provided by game characters require lots of reading, comprehension and even decision making.</LI>
<LI><I>Spelling:</I> Our chat filter REQUIRES that every word a player speaks is found in our 'clean dictionary'. This is a continuous spelling exercise, as the child cannot successfully communicate unless they properly spell.</LI>
<LI><I>Finance:</I> Many expensive items in the world require saving up lots of game money. Banking system includes interest and a simple stock market system.</LI>
<LI><I>Politics:</I> Club system requires players work together to be successful in a hierarchical system.</LI>
<LI><I>Social:</I> Multiplayer minigames and chat allow players to interact with other real people from around the world.</LI>
<LI><I>Horses:</I> Lots of accurate horse information, artwork and breed histories.</LI>
<LI><I>Brain Teasers:</I> Many riddles and other brain teasers stretch the mind a bit.</LI>
<LI><I>Educational Games:</I> A wide range of subjects from History, Physics, Programming, Math, etc. are directly used in some games.</LI> 
